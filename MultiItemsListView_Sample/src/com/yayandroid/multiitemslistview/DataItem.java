package com.yayandroid.multiitemslistview;

public class DataItem {

	public String data1, data2, data3;
	
	public DataItem (String data1, String data2, String data3) {
		this.data1 = data1;
		this.data2 = data2;
		this.data3 = data3;
	}
	
	public DataItem (String data1, String data2) {
		this.data1 = data1;
		this.data2 = data2;
	}
	
	public DataItem (String data1) {
		this.data1 = data1;
	}
	
}
